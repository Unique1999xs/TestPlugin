def calculate_area_of_circle(radius):
    pi = 3.14159
    area = pi * radius * radius
    return area

def calculate_area_of_circle_again(radius):
    pi = 3.14159
    area = pi * radius * radius
    return area

# 调用两个重复的函数
print(calculate_area_of_circle(5))
print(calculate_area_of_circle_again(5))